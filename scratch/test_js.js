function getSkillsScore(skills) {
    const req_embedded = ["embedding", "sentence-transformer", "bge", "e5", "rag", "retrieval", "semantic search"];
    const req_vector = ["pinecone", "weaviate", "qdrant", "milvus", "opensearch", "elasticsearch", "faiss", "vector database"];
    const req_python = ["python"];
    const req_eval = ["ndcg", "mrr", "map", "eval", "a/b test", "ranking", "re-ranking", "search evaluation"];
    const nice_to_have = ["lora", "qlora", "peft", "fine-tune", "xgboost", "neural ranking", "learning to rank", "distributed systems", "inference optimization", "hr-tech", "recruiting tech"];
    
    const skills_lower = skills.map(s => (s.name || '').toLowerCase());
    const has_embedded = req_embedded.some(kw => skills_lower.some(s => s.includes(kw)));
    const has_vector = req_vector.some(kw => skills_lower.some(s => s.includes(kw)));
    const has_python = req_python.some(kw => skills_lower.some(s => s.includes(kw)));
    const has_eval = req_eval.some(kw => skills_lower.some(s => s.includes(kw)));
    
    let score = 0.0;
    if (has_embedded) score += 0.25;
    if (has_vector) score += 0.25;
    if (has_python) score += 0.25;
    if (has_eval) score += 0.25;
    
    let bonus = 0.0;
    skills_lower.forEach(s => {
        if (nice_to_have.some(kw => s.includes(kw))) {
            bonus += 0.05;
        }
    });
    return Math.min(1.0, score + bonus);
}

